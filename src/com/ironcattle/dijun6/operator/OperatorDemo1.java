package com.ironcattle.dijun6.operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        // 加法
        int num1 = 1;
        int num2 = 2;
        int sum1 = num1 + num2;
        System.out.println("num1 + num2 = " + sum1);
        // 减法
        int num3 = 3;
        int num4 = 2;
        int sum2 = num3 - num4;
        System.out.println("num3 - num4 = " + sum2);
        // 乘法
        int num5 = 5;
        int num6 = 2;
        int sum3 = num5 * num6;
        System.out.println("num5 * num6 = " + sum3);
        // 除法
        int num7 = 7;
        int num8 = 2;
        int sum4 = num7 / num8;
        System.out.println("num7 / num8 = " + sum4);
        // 取余
        int num9 = 9;
        int num10 = 2;
        int sum5 = num9 % num10;
        System.out.println("num9 % num10 = " + sum5);
    }
}
